numero1 = float(input("Digite um numero: "))
numero2 = float(input("Digite outro numero: "))

soma = numero1 + numero2
subtracao = numero1 - numero2
multiplicacao = numero1 * numero2
divisao = numero1 / numero2

print("A soma dos dois numero é " + str(soma))
print("A subtração do primeiro numero pelo segundo numero " + str(subtracao))
print("A multiplicação dos dois numero é " + str(multiplicacao))
print("A divisão do primeiro numero pelo segundo numero " + str(divisao))
